      <section id="faq_shift_codes">
        <header>
          <h2>SHiFT Codes</h2>
        </header>
        <div class="qa" id="faq_shift_codes_q1">
          <h3>What are SHiFT Codes</h3>
          <p>SHiFT Codes are 25-character keys that grant in-game rewards.</p>
        </div>
        <div class="qa" id="faq_shift_codes_q2">
          <h3>Where are SHiFT Codes released?</h3>
          <p>SHiFT Codes are typically released on Twitter and Facebook via official handles, but are also sometimes released during special events and livestreams.</p>
        </div>
        <div class="qa" id="faq_shift_codes_q3">
          <h3>How often are SHiFT Codes released?</h3>
          <p>SHiFT Codes are typically released every Friday around 10AM PST.</p>
        </div>
        <div class="qa" id="faq_shift_codes_q4">
          <h3>Do SHiFT Codes ever expire?</h3>
          <p>Yes, most SHiFT Codes will be issued with an Expiration Date, typically about two to three weeks after their release. However, some SHiFT Codes have and can be issued without an Expiration Date.
        </div>
        <div class="qa" id="faq_shift_codes_q5">
          <h3>How many times can a SHiFT Code be redeemed?</h3>
          <p>SHiFT Codes can be redeemed once per account.</p>
        </div>
        <div class="qa" id="faq_shift_codes_q6">
          <h3>What games support SHiFT Codes?</h3>
          <p>The following titles support SHiFT Codes:</p>
          <ul>
            <li>Borderlands 2</li>
            <li>Borderlands: The Pre-Sequel</li>
            <li>Borderlands: Game of the Year Edition</li>
          </ul>
          <p><em>There are other titles that accept SHiFT Codes, such as Battleborn, but they are not supported on ShiftCodesTK.</em></p>
        </div>
      </section>
